            I trace a research thread that began with sensory expression and creative play with colleagues in Mexico and
            has grown into a body of work that takes leisure, pride, and participation seriously as first-order research
            concerns. Across this research, we see how long-term engagement with people allows us to see a more complete
            picture of them while creating more useful and more joyful technologies.
        </p>

